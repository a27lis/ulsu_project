      <p class="history-text">The USA is a presidential republic.</p>
      <p class="history-text">
        The USA is a very large country, so it has several different climatic
        regions. The coldest regions are in the north and north-east. The south
        has a subtropical climate. The United States is a land of rivers and
        lakes. The northern state of Minnesota is a land of 10 000 lakes. The
        longest rivers in the USA are the Mississippi, the Missouri and the Rio
        Grande. The highest mountains are the Rocky Mountains, the Cordillera
        and the Sierra Nevada.
      </p>
      <p class="history-text">
        The legislative branch of the US Government, or the Congress, represents
        all of the American states. It consists of two parts: the House of
        Representatives and the Senate. Each state has two senators, who are
        elected every 6 years. A senator must be at least 30 years old, a
        citizen of the United States for 9 years, and live in the state she or
        he will represent. A representative must be at least 25 years old, a
        citizen for 7 years, and live in the state. The job of the Congress is
        to make laws. The President can veto a bill. The Congress can pass the
        law anyway if it gets a two-thirds majority vote. The Congress can also
        declare war. The House of Representatives can also impeach the
        President. This means that the House can charge the President with a
        crime. In this case, the Senate will put the President on trial. The
        Senate votes to approve the justices that the President appoints to the
        Supreme Court.
      </p>
      <p class="history-text">
        The executive branch of the government puts the country’s laws into
        effect. The President of the United States is a member of the executive
        branch. The President must be at least 35 years old, and be a natural
        citizen of the USA. In addition, he must have lived in the US for at
        least 14 years, and be a civilian. The President is elected every four
        years and cannot serve more that two terms. The Vice-President of the
        USA is president of the Senate. When the President receives a bill from
        the Congress, he must sign it, and then the bill becomes a law. However,
        if he disagrees with the law, he can veto it. The President can also ask
        the Congress to declare war. He also appoints the justices to the
        Supreme Court. He must do his job according to the Constitution, or he
        may be impeached.
      </p>
      <p class="history-text">
        The judicial branch of the government is the system of courts in the
        United States. Its job is to enforce laws. The Supreme Court is the
        highest court in the country. It consists of 9 justices: one Chief
        Justice and 8 associate justices. The President appoints the justices,
        but the Senate must approve them. The justices are appointed for life.
        The Supreme Court makes sure that people obey the laws. The Supreme
        Court can also decide if a law is constitutional, that is, if it is in
        agreement with the Constitution. The judicial branch works together with
        the legislative and executive branches to protect the Constitution and
        the rights of people.
      </p>
